			<strong> Lagos declares Thursday work-free due to President Muhammad Buhari’s visit.</strong>
<p>The Lagos State Government has declared Thursday, March 29, 2018, as a work-free day in the state.
A statement by the Commissioner for Information and Strategy, Mr. Kehinde Bamigbetan, explained that “the holiday will enable Lagosians to come out en masse to welcome President Muhammadu Buhari” who is on a two-day (Thursday and Friday) visit to the state. Announcing traffic diversions and alternative routes ahead of Buhari’s visit, the state Commissioner of Police, Edgal Imohimi, at a press briefing in Lagos House, Ikeja, said adequate preparations had been made to mitigate the impact of road diversions on residents, while access would be given to any emergency situation.
Imohimi said the President would open the new Ikeja Bus Terminal, attend a colloquium in honour of a national leader of the All Progressives Congress, Asiwaju Bola Tinubu, and embark on a tour of the Eko Atlantic City, among others. </p>

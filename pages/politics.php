            <h1>Atiku knocks APC, says FG has failed Nigerians</h1>
            <div class="post_commentbox"> <a href="#"><i class="fa fa-user"></i></a> <span><i class="fa fa-calendar"></i></span> <a href="#"><i class="fa fa-tags"></i>Politics</a> </div>
            <div class="single_page_content"> <img class="img-center" src="../images/Atiku-Abubakar-71-615x344.jpg" alt="">
              <p> FORMER Vice-President Atiku Abubakar on Tuesday declared his intention to contest the position of the President. Atiku made this declaration when he led some leaders of the People’s Democratic Party to the Government House in Port Harcourt, the Rivers State capital. This is as the Rivers State Governor, Nyesom Wike, said Nigerians must take up the responsibility of sacking the “non-performing” APC Federal Government in 2019.
The former Vice-President and other PDP leaders were in the state to consult with chieftains of the party in Rivers State. Atiku said that he was offering himself to set the country on the path of growth after “the serial failure of the APC.” He said that since 2015, the APC Federal Government had destroyed the education, health and infrastructural sectors through poor budgetary allocations and releases.
“Since 2015, we have not seen a 100-kilometre road constructed by the APC Federal Government anywhere in the country,” Atiku said. He pointed out that the APC Federal Government had destroyed the foundation of unity and cohesion of the country. “I have never seen Nigeria so divided along religious, ethnic and regional lines. This division is as a result of the mismanagement of the APC,” the former Vice-President added.
The presidential aspirant expressed the need for the unity of the country, saying that no component part of the country could progress alone. Atiku urged the Rivers PDP stakeholders to support his ambition to emerge as the party’s presidential candidate. In his remarks, Wike urged all PDP members irrespective of their status to ensure that the march to the Presidential Villa was not truncated by disunity.
He urged presidential aspirants on the platform of the PDP to contest within acceptable limits, adding that the race should not be a do-or-die affair, but a movement to oust a failed APC Federal Government. Wike said, “Every presidential aspirant must see himself as a member of the larger PDP family. We must do everything to ensure that the PDP returns to the Presidential Villa in 2019. “Only one aspirant will become a candidate. We will do everything to ensure that the party comes out with a candidate that has a track record to upstage the APC,” he added.
The governor said that he had received a former Jigawa State governor, Sule Lamido, and the former Vice-President, pointing out that other aspirants would be offered the platform to consult with the Rivers PDP members. “God will make sure that the right candidate emerges. When the right candidate emerges, we will team up and remove this bad government. “There is no benefit that Rivers State has got from this APC Federal Government. All the APC Federal Government is doing is plotting to rig the 2019 elections,” Wike said.
He said that the former Vice-President had the right qualification to fly the PDP flag, even as he advised him to extend his consultations. Also speaking, a former Enugu State governor, Dr. Okwesilieze Nwodo, described Atiku as a detribalised Nigerian with the capacity to rescue the country. In his remarks, ex-governor of Ogun State, Otunba Gbenga Daniel, said Atiku remained a true Nigerian with business and political links across the country. According to him, Nigeria needs Atiku to revive the country.

.</p>

			 <a class="sideAdd" href="#"><img src="../images/nnpc.jpg" alt=""></a> </div>
			<strong></strong>
			<br>
			<p>
Amid confusion and disagreement over discrepancies observed in the revenue figures presented by the Nigerian National Petroleum Corporation (NNPC), the Federation Accounts Allocation Committee (FAAC) meeting ended on Tuesday in deadlock in Abuja. The meeting was convened for representatives of the 36 states of the federation and the Federal Capital Territory to consider and approve statutory allocation to the tiers of government for February.
Consequently, the committee said it could not approve the allocations to the three tiers of government, describing the discrepancies in revenue figures presented by the NNPC as a bad omen to workers who may have to go without salaries for the month. The Accountant-General of the Federation, Ahmed Idris, confirmed the development to reporters shortly after the meeting at the headquarters of the Federal Ministry of Finance, Abuja. 



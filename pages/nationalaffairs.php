            <h1>We Informed Federal Government, Military of Threats, Soldiers’ Misconduct, Says Taraba Government</h1>
            <div class="post_commentbox"> <a href="#"><i class="fa fa-user"></i></a> <span><i class="fa fa-calendar"></i></span> <a href="#"><i class="fa fa-tags"></i>Politics</a> </div>
            <div class="single_page_content"> <img class="img-center" src="../images/Governor-Darius-Ishaku.jpg" alt="">
              <p> The Taraba State Government on Tuesday said the Federal Government and the military were aware of security threats, as well as soldiers’ misconduct in the state, but chose to do nothing about them. In a statement by the Senior Special Assistant to the Governor of Taraba State on Media and Publicity, Mr. Bala Abu, the Taraba State Government cited few instances that the Federal Government and the military were informed about security breach and soldiers’ misconduct, which were allegedly ignored. 
The state government’s statement was in reaction to the claim made by the Federal Government on Monday that although it got reports of soldiers’ misconduct, there was none from Taraba State. Col. Tukur Gusau, the spokesperson for the Minister of Defence, Mansur Dan-Ali, who spoke in an interview with one of our national daily newspaper on behalf of his principal, had said, “Nigerians have been coming to report incidents and we have been taking care of them… Soldiers have been put through disciplinary procedures.
“We have not received anything yet from Taraba State. We need to work on specific things. To my knowledge, we didn’t receive anything in respect of our operations in Taraba State.” But the Taraba State Government, in its reaction, warned that thousands of people might still be killed by herdsmen unless the military turned a new leaf. It said, “The case against the military in Taraba is that of outright collusion against the people which has made it easy for the Fulani militia to kill and destroy property. It is also a case of disrespect for the Office of the Governor of Taraba State.”
Abu in the statement explained that in February 2016, the Ministry of Interior influenced the visit of military investigators to investigate an allegation against a monarch in the state, Dr. Shekarau Masa-Ibi. He stated that the military delegation “had no courtesy to inform the state governor” on the visit and purpose of the visit. “The Taraba State Government protested this obvious act of disrespect… in a letter to the Chief of Army Staff, dated February 23, 2016,” he added. Abu noted that the state Governor, Darius Ishaku, also wrote the President on January 26, 2016, to complain about threat to peace and security in Taraba State. “In that letter signed by Governor Ishaku himself, he lamented the devastating effects of internal conflicts involving the Fulani and Tiv, which led to the sacking of 200 settlements in the Gassol, Bali, Ibi, Donga and Gashaka local government areas by herdsmen. “The governor informed the presidency about the concerns raised by some traditional rulers in the state concerning the influx of Fulani militia and about the attacks on their communities. Letters of complaints from the traditional rulers whose communities suffered from these attacks were also attached and forwarded to the presidency… Copies of this letter were sent to the National Security Adviser, Chief of Staff to the President and the Inspector-General of Police,” he added.
According to Abu, Ishaku wrote another letter to the then Acting President, Prof. Yemi Osinbajo, on July 5, 2017, drawing his attention to “the precarious security situation as contained in a letter written by contractors handling the Kashimbilla Dam project and called for high level intervention. He called on the army, the Navy and Air Force to establish permanent security bases in the area. This was again ignored.”
The Taraba State Government said it reported the misconduct of the Commanding Officer of 93 Battallion, Ada Barracks in Takum on several occasions, but no action was taken against him by the military. “For example, the Fulani militia attacked communities in Takum and Ussa on May 6, 2017. The crisis led to the abandonment of 224 cattle belonging to the Fulani herdsmen. “The Taraba State Government took possession of these cows and handed them over to the Commanding Officer for safe keeping until the owners return to collect. This was meant to be a ploy to get the perpetrators of the crisis arrested. Sadly, the Commanding Officer released the cows without arresting anybody.
“There was another case when the commanding officer marched soldiers to attack and brutalise communities in Kashimbilla. Property of the people was damaged while many were injured. “The letter said the Commanding Officer was partial and discriminatory in the discharge of his duties and requested that he should be transferred. The advice was ignored,” the statement said. The governor’s spokesman said Ishaku had always raised the alarm on security situations in the state, but was always ignored. He said, “On January 30, this year, The governor wrote another letter to the Vice President to again complain about the attitude of the Commanding Officer of 93 Battalion, Takum, Lt. Col. Ibrahim Gambari, whose soldiers always looked the other way when the herdsmen militia come to kill.
“The governor said the security situation in the country and in Taraba State demanded that every security officer cooperate and take directives from the chief security officer of the state which is the governor, but lamented that this has not been the case with the commanding officer. “The letter listed instances of security challenges in which the military failed to live up to expectations. “The letter also alerted the military authorities to a planned massive movement of Fulani and their cows into Takum LGA, adding that motive was to provoke the people and precipitate crisis. The military in-charge of security in the area did nothing. “The widely publicised report on social media and which was investigated and confirmed to the effect that a chopper dropped arms in a village near Wukari was downplayed by the security agencies.
“Despite efforts by the Taraba State Government to get the military to act, they never did. Since then, the arms and ammunition brought into the state have been used against the people in various communities in the state by the herdsmen. “The present Operation Ayem Akpatuma in the state has also been discriminatory. While cutlasses and knives have been taken away from the people, the herdsmen have been left with AK-47 rifles. “Through these various acts of deliberate mischief on the part of the military thousands of people have been killed and a lot more may be killed unless the military turns a new leaf.”  No letter from Taraba, FG, military insist
In their reactions, the Ministry of Defence and the Nigerian Army said they had yet to receive any official complaints from the Taraba State Government against troops in the state. Spokesman for the Minister of Defence said the ministry welcomed complaints from the state government, adding that the military “would work promptly on the issues.”  Gusau said, “To the best of my knowledge, the Minister of Defence has not received any letter of such nature. Let them bring the acknowledged copy of the letters to the ministry.
“The operations we are conducting are joint operations; the military is working with other security agencies. “We welcome anybody with grievances against how the military is conducting its operations. They should not be on the pages of newspaper. Let them write to us and we will take it up.”  Also, the Director, Army Public Relations, Brig-Gen Texas Chukwu, said, “We have not seen any letter (from Taraba).”



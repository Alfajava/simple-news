            <h2><span>Danjuma: Ortom backs self-defence call</span></h2>
			 <a class="sideAdd" href="#"><img src="../images/Danjuma.jpeg" alt=""></a> </div>
			
<p> The Benue State Governor, Samuel Ortom, on Tuesday backed a former Minister of Defence, Theophilus Danjuma, on his call on Nigerians to defend themselves in the face of killings across the country. Ortom gave his support to the call in an interview with State House correspondents after his meeting with President Muhammadu Buhari at the Presidential Villa, Abuja.
While saying that Danjuma did not ask people to take up arms, the governor explained that the former Chief of Army Staff only said people should protect themselves. He said, “Of course, you know that self-preservation is the first law of nature. “You can’t just wait in your house and allow someone to come and kill you. You can resist the person, but you don’t necessarily need to have arms.
“I think that was what the former minister was saying. There is a need for people to arise to defend themselves not necessarily by using weapons that are banned. “You can use a stick and disarm someone. Some years ago, I was able to disarm armed robbers bearing AK 47 rifles without anything. I think that is the kind of thing he is trying to say. Nigerians should rise up to do this.
“Security matters are not just left in the hands of security men, it requires every one of us to put our heads together and be courageous to be able to surmount it.” The governor said some fresh killings had been recorded in his state after Buhari visited Benue State recently. “Well, there are some killings in flashpoints by these militia groups, the security men are doing their best, and a number of arrests have been made by the security agencies.
“It is still going on, even yesterday (Monday), there were killings but the security men are doing their best. I believe that we will overcome this. “If not for the presence of the security men in our land, the situation would have really been worse than what it is today. I think that we are collaborating with the security agencies and not leaving any stone unturned,” he added.
Ortom said he visited Buhari to thank him for his recent visit and to remind him about the requests made by the people of the state during the visit. He said he briefed the President on the plight of over 175,000 Internally Displaced Persons in eight camps in the state. The governor said the IDPs had expressed their desire to go back home.
He said Buhari assured him that government would ensure that the houses from where they were displaced were rehabilitated. The governor insisted that the 20 million cattle in Nigeria could not cope with open grazing. 
